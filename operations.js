function sum(number1, number2) {
  const resultsFromSum = number1 + number2;
  return resultsFromSum;
}

function subtract(number1, number2) {
  const resultsFromSubtract = number1 - number2;
  return resultsFromSubtract;
}

function divide(number1, number2) {
  const resultsFromDivide = number1 / number2;
  return resultsFromDivide;
}

function multiply(number1, number2) {
  const resultsFromMultiply = number1 * number2;
  return resultsFromMultiply;
}
